#include <iostream>
using namespace std;

class Solution {
public:
    int findNthDigit(int n) {
        long long len = 1, cnt = 9, start = 1;
        while (n > len * cnt) {
            n -= len * cnt;
            ++len;
            cnt *= 10;
            start *= 10;
        }
        start += (n - 1) / len;
        
        long long temp = start / (int)pow(10,(len - 1) - (n - 1) % len);
        return temp % 10;
    }
};